				<div><h3>História da Instituição</h3>
					<p>A Escola Louis Braille foi idealizada pelo Dr. Guilherme Echenique Filho, e nasceu como departamento da Biblioteca Pública Pelotense. A primeira professora da Escola foi uma jovem cega chamada Lory Huber e a partir de sua contratação foi realizada uma chamada aos cegos da cidade, e no dia 10 de junho de 1952 a escola foi fundada com apenas seis alunos. O material didático foi todo confeccionado em Pelotas com a ajuda de Dona Ignez Figueiredo, que muito colaborou com a escola. Esse material constava de mapas, cartilhas e coisas do gênero, sendo que os aparelhos especializados eram de propriedade de Lory Huber, dos quais ela tinha uma pequena coleção: regletes e punções para escrever e cubarítmos, para os cálculos de matemática. Na escola, Lory Huber ensinava os alunos a ler e escrever, a calcular, e dava, ainda, aulas de datilografia trabalhos manuais, canto e música. Ensinava, em suma, tudo o que ela sabia.
					</p>
					<p>Contava sempre com o máximo apoio do Lions Club de Pelotas, das autoridades educacionais do Município e do Estado, e só assim, conseguiu levar avante esse grande ideal.
					</p>
					<p>Dez anos após sua fundação a Escola que iniciou como um departamento da Biblioteca Pública adquiriu personalidade jurídica própria. Em 1963, passou a funcionar em prédio próprio construído pelo Estado em terreno doado pelo Município na Rua Andrade Neves, 3084.
					</p>
					<p>Daí em diante, a Escola Louis Braille passou a desenvolver-se e aprimorar-se na educação e na inclusão da pessoa com deficiência visual.
					</p>
